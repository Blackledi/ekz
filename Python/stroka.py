# На вход программе подаётся одна строка. 
# Напишите программу, которая определяет сколько 
# в ней одинаковых соседних символов.

s = input()  
count = 0  

# Проходим по всем символам строки, кроме последнего
for i in range(len(s) - 1):
    if s[i] == s[i + 1]:  # Если текущий символ равен следующему
        count += 1  # Увеличиваем счётчик

print(count)  

