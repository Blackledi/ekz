# На вход программе подаётся натуральное число, 
# записанное в десятичной системе счисления. 
# Напишите программу, которая переводит данное число 
# в двоичную систему счисления.

n = int(input())  
binary = ''  # Переменная для хранения двоичного представления

while n > 0:  # Пока число больше 0
    binary = str(n % 2) + binary  # Находим остаток от деления на 2 и добавляем его к результату
    n //= 2  

print(binary)  

